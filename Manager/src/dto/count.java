package dto;

public class count {
	private int cnum;
	private int cnt;
	public count(int cnum, int cnt) {
		super();
		this.cnum = cnum;
		this.cnt = cnt;
	}
	public int getCnum() {
		return cnum;
	}
	public void setCnum(int cnum) {
		this.cnum = cnum;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
}
